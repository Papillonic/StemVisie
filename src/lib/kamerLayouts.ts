/* =========================================================
   HELPERS
   ========================================================= */

export const allLayouts: ChamberLayoutPeriod[] = [
  kamerLayoutNov2023_Nov2025,
  kamerLayoutNov2025_Jan2026,
  kamerLayoutJan2026_Feb2026,
  kamerLayoutVanafFeb2026,
];

export const getLayoutForDate = (
  date: string
): ChamberLayoutPeriod | undefined => {
  const d = new Date(date);

  return allLayouts.find((layout) => {
    const start = new Date(layout.startDate);
    const end = layout.endDate ? new Date(layout.endDate) : null;

    return d >= start && (!end || d <= end);
  });
};